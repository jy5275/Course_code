#include <iostream>
#include <string>
using namespace std;
template<class T>
class CMyistream_iterator {
	typedef CMyistream_iterator MYIS_IT;
	istream &is;
	T *pVal = NULL;   //当前指针pVal
public:
	CMyistream_iterator(istream &i) :is(i) {}
	void operator++(int k) { pVal = NULL; }
	T operator*() {
		if (pVal != NULL)
			return *pVal;  //已经有读取值了，返回回去！
		else {
			pVal = new T;
			is >> *pVal;
			return *pVal;
		}
	}
};

